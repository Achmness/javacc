    private void jPanel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel3MouseClicked

String diagnosis = r_diagnosis.getText().trim();
String instructions = r_instructions.getText().trim();
String prescription = r_prescription.getText().trim();
String notes = r_notes.getText().trim();

if(diagnosis.isEmpty() || instructions.isEmpty() || prescription.isEmpty() || notes.isEmpty()){
    JOptionPane.showMessageDialog(this, "Please fill in all fields.");
    return;
}

config db = new config();

try (Connection conn = db.connectDB()) {
    // You have 5 question marks, so you MUST have 5 insert.set... calls
    String sql = "UPDATE records SET r_diagnosis=?, r_instructions=?, r_prescription=?, r_notes=? WHERE r_id=?";

    PreparedStatement updateStmt = conn.prepareStatement(sql);
    updateStmt.setString(1, diagnosis);
    updateStmt.setString(2, instructions);
    updateStmt.setString(3, prescription);
    updateStmt.setString(4, notes);
    updateStmt.setInt(5, recordId); 

    updateStmt.executeUpdate();

    JOptionPane.showMessageDialog(this, "Medical Record Updated Successfully!");
    JFrame mainFrame = (JFrame) javax.swing.SwingUtilities.getWindowAncestor(this);
    
    vet vet = new vet();
    vet.setVisible(true);
this.dispose();
} catch (Exception e) {
    e.printStackTrace();
}
    }//GEN-LAST:event_jPanel3MouseClicked

