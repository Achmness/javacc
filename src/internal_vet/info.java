           public int getTotalPending() {
    int total = 0;

    config db = new config();

    try (Connection conn = db.connectDB()) {

        String sql = "SELECT COUNT(*) FROM appointment WHERE ap_status = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, "Pending");

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            total = rs.getInt(1);
        }

    } catch (Exception e) {
        e.printStackTrace();
    }

    return total;
}
       
    public int getTotalTodayAppointments() {
    int total = 0;

    config db = new config();
    try (Connection conn = db.connectDB()) {

        // Get today's date in yyyy-MM-dd format
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
        String today = sdf.format(new java.util.Date());

        // ✅ Only count appointments with status 'Accepted'
        String sql = "SELECT COUNT(*) FROM appointment WHERE ap_date = ? AND ap_status = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, today);
        pst.setString(2, "Approve");

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            total = rs.getInt(1);
        }

    } catch (Exception e) {
        e.printStackTrace();
    }

    return total;
}
    
