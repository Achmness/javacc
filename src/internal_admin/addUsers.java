    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
 
        String userad = status.getText().trim();
        String emailad = email.getText().trim();
        String fnamead = fname.getText().trim();
        String lnamead = lname.getText().trim();
        String contactad = contact.getText().trim();
        String userTypead = userType.getText().trim();
        String addressad = address.getText().trim();
        String statusad = status.getText().trim();

        String passwordup = "qwe123";

        if (userad.isEmpty() || emailad.isEmpty() || fnamead.isEmpty() || lnamead.isEmpty() || contactad.isEmpty() || userTypead.isEmpty() || addressad.isEmpty() || statusad.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields");
            return;
        }
        if (!config.isValidEmail(emailad)) {
            JOptionPane.showMessageDialog(
                this,
                "Invalid email format!\nExample: test@gmail.com"
            );
            return;
        }

        String hashedPassword = config.hashPassword(passwordup);

        if (hashedPassword == null) {
            JOptionPane.showMessageDialog(this, "Error processing password");
            return;
        }
        config db = new config();
        String checkSql = "SELECT * FROM account WHERE a_email = ?";
        java.util.List<java.util.Map<String, Object>> existing = db.fetchRecords(checkSql, emailad);
        if (!existing.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email already exists. Choose another.");
            return;
        }

        String insertSql = "INSERT INTO account(a_user, a_email, a_pass, a_fname, a_lname, a_contact, a_address, a_type, a_status) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ? )";
        db.addRecord(insertSql, userad, emailad, hashedPassword, fnamead, lnamead, contactad, addressad, userTypead, statusad);
        JOptionPane.showMessageDialog(this, "Account Added Successfully!");
        users u = new users();
        admin admin = new admin(u);
        admin.setVisible(true);

    }//GEN-LAST:event_jButton2ActionPerformed

