    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
            try {

    String userEmail = email.getText().trim();

    if(userEmail.isEmpty()){
        JOptionPane.showMessageDialog(null, "Please enter your email.");
        return;
    }

    // Check if email exists in database
    String sql = "SELECT * FROM account WHERE a_email = ?";
    PreparedStatement pst = conn.prepareStatement(sql);
    pst.setString(1, userEmail);
    ResultSet rs = pst.executeQuery();

    if(rs.next()){

        // Generate OTP
        Random rand = new Random();
        randomCode = 100000 + rand.nextInt(900000);

        String host = "smtp.gmail.com";
        String user = "arknigh38@gmail.com";
        String pass = "xsmkdyobwoduwnde";

        String to = userEmail;
        String subject = "Password Reset Code";
        String message = "Your reset code is: " + randomCode;

        Properties pros = System.getProperties();
        pros.put("mail.smtp.starttls.enable", "true");
        pros.put("mail.smtp.host", host);
        pros.put("mail.smtp.port", "587");
        pros.put("mail.smtp.auth", "true");

        Session mailSession = Session.getDefaultInstance(pros, null);

        Message msg = new MimeMessage(mailSession);
        msg.setFrom(new InternetAddress(user));
        InternetAddress[] address = { new InternetAddress(to) };
        msg.setRecipients(Message.RecipientType.TO, address);
        msg.setSubject(subject);
        msg.setText(message);

        Transport transport = mailSession.getTransport("smtp");
        transport.connect(host, user, pass);
        transport.sendMessage(msg, msg.getAllRecipients());
        transport.close();

        JOptionPane.showMessageDialog(null, "Reset code sent to your email.");

    }else{
        JOptionPane.showMessageDialog(null, "Email not registered.");
    }

}catch(Exception ex){
    JOptionPane.showMessageDialog(rootPane, ex);
}        
    }//GEN-LAST:event_jLabel3MouseClicked

